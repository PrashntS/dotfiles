func main() {
	rand.Seed(time.Now().UnixNano())
	LMouth, LEyes, LEars := len(Mouths), len(Eyes), len(Ears)
	app := cli.NewApp()
	app.Name = "dafaq.lol"
	app.Version = "0.1.0"
	app.Usage = fmt.Sprintf("generate them emotes. %d and counting", (LMouth * LEyes * LEars))

	app.Action = func(c *cli.Context) error {
		r1, r2, r3 := rand.Intn(LMouth), rand.Intn(LEyes), rand.Intn(LEars)

		if rand.Intn(100) < 42 {
			color.Set(color.FgYellow)
		} else {
			color.Set(color.FgGreen)
		}

		fmt.Print("\n\t", Ears[r3][0], Eyes[r2][0], Mouths[r1], Eyes[r2][1], Ears[r3][1], "\n\n")

		color.Unset()
		return nil
	}

	app.Run(os.Args)
}
